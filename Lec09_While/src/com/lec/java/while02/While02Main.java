package com.lec.java.while02;

public class While02Main {

	public static void main(String[] args) {
		System.out.println("while 연습");
		
		int count = 1;
		
		while(count < 10) {
			System.out.println("2 *"+count+"="+2*count);
			count++;  
		}


	} // end main()

} // end class While02Main









